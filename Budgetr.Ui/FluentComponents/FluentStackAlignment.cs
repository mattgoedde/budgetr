
/// <summary>
/// Determines the horizontal alignment of the content within the <see cref="Stack"/>.
/// </summary>
public enum FluentStackHorizontalAlignment
{
    /// <summary>
    /// The content is aligned to the left.
    /// </summary>
    Left,

    /// <summary>
    /// The content is center aligned.
    /// </summary>
    Center,

    /// <summary>
    /// The content is aligned to the right.
    /// </summary>
    Right,
}

/// <summary>
/// Determines the vertical alignment of the content within the <see cref="Stack"/>.
/// </summary>
public enum FluentStackVerticalAlignment
{
    /// <summary>
    /// The content is aligned to the top.
    /// </summary>
    Top,

    /// <summary>
    /// The content is center aligned.
    /// </summary>
    Center,

    /// <summary>
    /// The content is aligned to the bottom
    /// </summary>
    Bottom,
}